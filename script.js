const LANGUAGE_KEY = 'hhc-lang';
const defaultLang = navigator.language.startsWith('es') ? 'es' : 'en';
let currentLang = localStorage.getItem(LANGUAGE_KEY) || defaultLang;

function applyTranslations(lang) {
  const elements = document.querySelectorAll('[data-i18n]');
  elements.forEach((el) => {
    const key = el.getAttribute('data-i18n');
    if (translations[lang] && translations[lang][key]) {
      el.textContent = translations[lang][key];
    }
  });
  currentLang = lang;
  localStorage.setItem(LANGUAGE_KEY, lang);
  document.documentElement.setAttribute('lang', lang);
  updateLangButtons();
}

function updateLangButtons() {
  document.querySelectorAll('.lang-btn').forEach((btn) => {
    btn.classList.toggle('lang-active', btn.dataset.lang === currentLang);
  });
}

function setupLanguageToggle() {
  document.querySelectorAll('.lang-btn').forEach((btn) => {
    btn.addEventListener('click', () => {
      const lang = btn.dataset.lang;
      applyTranslations(lang);
    });
  });
  applyTranslations(currentLang);
}

function setupCopyButtons() {
  const buttons = document.querySelectorAll('[data-copy-target]');
  buttons.forEach((btn) => {
    btn.addEventListener('click', async () => {
      const targetId = btn.getAttribute('data-copy-target');
      const addressEl = document.getElementById(targetId);
      const feedback = document.querySelector(`[data-feedback-for="${targetId}"]`);
      const text = addressEl?.textContent?.trim();
      if (!text) return;

      try {
        await navigator.clipboard.writeText(text);
        if (feedback) {
          feedback.textContent = translations[currentLang].copySuccess;
          feedback.classList.add('visible');
          setTimeout(() => {
            feedback.textContent = translations[currentLang].copyReady;
            feedback.classList.remove('visible');
          }, 2000);
        }
      } catch (err) {
        if (feedback) {
          feedback.textContent = translations[currentLang].copyError;
          feedback.classList.add('visible');
          setTimeout(() => {
            feedback.textContent = translations[currentLang].copyReady;
            feedback.classList.remove('visible');
          }, 2000);
        }
      }
    });
  });
}

function setupSmoothScroll() {
  document.querySelectorAll('a[href^="#"]').forEach((link) => {
    link.addEventListener('click', (e) => {
      const targetId = link.getAttribute('href').substring(1);
      const target = document.getElementById(targetId);
      if (target) {
        e.preventDefault();
        target.scrollIntoView({ behavior: 'smooth' });
      }
    });
  });
}

document.addEventListener('DOMContentLoaded', () => {
  setupLanguageToggle();
  setupCopyButtons();
  setupSmoothScroll();
});
