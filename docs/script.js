// Determine initial language based on browser setting
let currentLang = (navigator.language || navigator.userLanguage || 'en').toLowerCase().startsWith('es') ? 'es' : 'en';

// Update text content of all elements with data-i18n attribute
function updateContent() {
  document.querySelectorAll('[data-i18n]').forEach(el => {
    const key = el.getAttribute('data-i18n');
    const translation = translations[currentLang][key];
    if (translation) {
      el.innerHTML = translation;
    }
  });
}

// Set language and update UI
function setLanguage(lang) {
  currentLang = lang;
  updateContent();
}

// Copy wallet address with feedback
function copyToClipboard(text, buttonId) {
  navigator.clipboard.writeText(text).then(() => {
    const button = document.getElementById(buttonId);
    const originalLabel = translations[currentLang][button.dataset.i18n];
    button.textContent = currentLang === 'es' ? '¡Copiado!' : 'Copied!';
    setTimeout(() => {
      button.textContent = originalLabel;
    }, 2000);
  });
}

document.addEventListener('DOMContentLoaded', () => {
  // Initial translation
  updateContent();
  // Language button handlers
  const btnEn = document.getElementById('lang-en');
  const btnEs = document.getElementById('lang-es');
  if (btnEn && btnEs) {
    btnEn.addEventListener('click', () => setLanguage('en'));
    btnEs.addEventListener('click', () => setLanguage('es'));
  }
});